// 编写程序，使用递减运算符在循环中按递减顺序打印出 10 到 0 之间的整数
//
// ============================================================

#include <iostream>

int main()
{
    int i = 11;
    while (i != 0) std::cout << --i << "\t";
    std::cout << std::endl;
    return 0;
}
